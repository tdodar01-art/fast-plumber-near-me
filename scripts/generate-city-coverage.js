#!/usr/bin/env node
/**
 * Generate src/lib/city-coverage.ts from plumbers-synthesized.json.
 * Counts plumbers within 20 miles of each city that has coordinates.
 * Run before build: node scripts/generate-city-coverage.js
 */

const fs = require("fs");
const path = require("path");

const SYNTH_PATH = path.join(__dirname, "..", "data", "synthesized", "plumbers-synthesized.json");
const COORDS_PATH = path.join(__dirname, "..", "src", "lib", "city-coords.ts");
const OUTPUT_PATH = path.join(__dirname, "..", "src", "lib", "city-coverage.ts");

function calcDist(lat1, lng1, lat2, lng2) {
  const R = 3958.8;
  const toRad = (d) => (d * Math.PI) / 180;
  const dLat = toRad(lat2 - lat1);
  const dLng = toRad(lng2 - lng1);
  const a = Math.sin(dLat / 2) ** 2 + Math.cos(toRad(lat1)) * Math.cos(toRad(lat2)) * Math.sin(dLng / 2) ** 2;
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}

// Load plumbers
const raw = JSON.parse(fs.readFileSync(SYNTH_PATH, "utf-8"));
const plumbers = raw.plumbers.filter(
  (p) => p.location?.lat && p.location?.lng && (!p.businessStatus || p.businessStatus === "OPERATIONAL")
);

// Parse city coords from TS file
const coordsFile = fs.readFileSync(COORDS_PATH, "utf-8");
const coordMatches = [...coordsFile.matchAll(/"([A-Z]{2}):([a-z0-9-]+)":\s*\[([0-9.-]+),\s*([0-9.-]+)\]/g)];
const coords = {};
for (const m of coordMatches) {
  coords[m[1] + ":" + m[2]] = [parseFloat(m[3]), parseFloat(m[4])];
}

// Count plumbers within 20 miles of each city
const coverage = {};
for (const [key, [lat, lng]] of Object.entries(coords)) {
  let count = 0;
  for (const p of plumbers) {
    if (calcDist(lat, lng, p.location.lat, p.location.lng) <= 20) count++;
  }
  if (count > 0) coverage[key] = count;
}

const output = `/**
 * Auto-generated by scripts/generate-city-coverage.js
 * Count of plumbers within 20 miles of each city.
 * Used by CitySearch to only show cities with actual plumber data.
 */

export const CITY_COVERAGE: Record<string, number> = ${JSON.stringify(coverage)};
`;

fs.writeFileSync(OUTPUT_PATH, output);
console.log(`City coverage: ${Object.keys(coverage).length} cities with data (${plumbers.length} plumbers)`);
